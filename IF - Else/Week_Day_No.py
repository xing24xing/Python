num = int(input("Enter Any Number From 1 to 7 To Get Week Corresponding To That Number\n"))
if num == 1:
    print("Sunday")
elif num == 2:
    print("Monday")
elif num == 3:
    print("Tuesday")
elif num == 4:
    print("Wednesday")
elif num == 5:
    print("Thursday")
elif num == 6:
    print("Friday")
elif num == 7:
    print("Saturday")
else:
    print("Wrong Input! Enter Number From 1 to 7 Only")
